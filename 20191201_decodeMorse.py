def decodeMorse(morse):
    # line_m = morse.strip().replace('   ', ' sp ').split(' ')
    output = ''
    for item in morse.strip().replace('   ', ' sp ').split(' '):
        if item == 'sp':
            output += ' '
        else:
            output += MORSE_CODE[item]
    return output

